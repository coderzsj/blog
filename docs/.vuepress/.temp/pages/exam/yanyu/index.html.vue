<p><strong>非典型标志词</strong>：殊不知，截然不同，截然相反，全新的研究，一种误读，相对而言</p>
<p><strong>错误选项特征：</strong></p>
<p>1 转折前的内容
2 例子、原因、背景。。。
3 无中生有
4 绝对表述
5 与文意相驳</p>
<p><strong>略读句子特征：</strong></p>
<p>例子：比如、例如、诸如、譬如等
原因：因为、由于、：、---等
背景：近年来、随着、在。。。背景(研究)下等</p>
